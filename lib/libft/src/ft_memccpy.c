#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	void	*p;
	size_t	len;

	p = ft_memchr(src, c, n);
	if (p != NULL)
	{
		len = p - src + 1;
		return ((char *)ft_memcpy(dest, src, len) + len);
	}
	ft_memcpy(dest, src, n);
	return (NULL);
}
