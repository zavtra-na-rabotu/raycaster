#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char *retval;
	unsigned char searchchar;

	retval = (unsigned char *)s;
	searchchar = (unsigned char)c;
	while (n)
	{
		if (*retval == searchchar)
		{
			return (retval);
		}
		retval++;
		n--;
	}
	return (NULL);
}
