#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int result;

	if (n < 0)
	{
		ft_putchar('-');
	}
	result = (unsigned int)FT_ABS(n);
	if (result > 9)
	{
		ft_putnbr(result / 10);
	}
	ft_putchar(result % 10 + '0');
}
