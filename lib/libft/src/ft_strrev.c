#include "libft.h"

void	ft_strrev(char *str)
{
	size_t	i;
	size_t	j;
	char	a;

	i = 0;
	j = ft_strlen((const char *)str) - 1;
	while (i < j)
	{
		a = str[i];
		str[i] = str[j];
		str[j] = a;
		i++;
		j--;
	}
}
